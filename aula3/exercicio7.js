var hojeVaiChover;

hojeVaiChover = true;

//opção bloco if...else

if (hojeVaiChover) {
  console.log("Leve seu guarda-chuva, hoje vai chover");
} else {
  console.log("Fique tranquilo, hoje será um dia de sol");
}

//opção ternário

hojeVaiChover
  ? console.log("Leve seu guarda-chuva, hoje vai chover")
  : console.log("Fique tranquilo, hoje será um dia de sol");
